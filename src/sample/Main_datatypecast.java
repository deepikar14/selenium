package sample;

public class Main_datatypecast {
public static void main(String[] args) {
	char c='a';
	int x=(int)c;
	System.out.println(x);
	
	int z=10;
	double a=(double)z;
	System.out.println(a);
	
	double d=30.4;
	int t=(int)d;
	System.out.println(t);
	
	int i=100;
	char g=(char)i;
	System.out.println(g);
}
}
